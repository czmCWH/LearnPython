# 👉 1、while 循环
# while 循环：当满足条件时，循环体内的代码会被执行多次 
# 语法结构：
# while 条件表达式:
#     循环体语句1
#     循环体语句2
#     ...
#     循环体语句n
# else:
#     循环条件为 False，循环正常结束后的操作
#
# 注意，else 语句，通常不需要。

i = 1
while i <= 10:
    print(f"人生苦短，我用Python：{i}")
    i += 1
else:
    print("循环正常结束")

print("----------------------------")

# 案例1：计算 1~100 之间所有偶数的和
i = 1
sum = 0
while i <= 100:
    if i % 2 == 0:  # 判断 i 是否为偶数
        sum += i
    i += 1
else:
    print(f"1~100 之间的偶数之和是：{sum}")

