# 1、什么是函数？
# 函数是组织好的、可重复使用的、用来实现特定功能的代码片段。
# Python 内置函数，如：input()、print()、type()、range() 等等

# 2、自定义函数
# def 函数名(参数列表):
#     函数体
#     ......
#     return 返回值
#   
# - 函数名的命名规则和变量名一致，建议使用小写字母和下划线。
# - 参数列表、返回值是可选的。

 
# 3、调用函数
#     函数名(参数列表)
#
# 调用函数时，需要传递给函数相应的参数，如果没有传递参数，那么该函数会使用默认值。

# 定义函数
def my_function():
    print("Hello from a function")

# 调用函数
my_function()
