# 👉 1、标识符
# 标识符，是指在代码中为变量、函数、类等元素所起的名字。
# 标识符命名规则：
#   - 由数字（0=9）、字母（a-z、A-Z）和下划线（_）组成。
#   - 第一个字符不能是数字。
#   - 严格区分字母大小写，即大写的字母和小写的字母是不同的变量。
#   - 不允许使用关键字作为标识符。如：True、False、None、if、else、for、while、def、class 等。


