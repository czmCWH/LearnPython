# 👉 3、变量使用案例
# 需求1：现有两个变量，分别为:a=10，b=20，现需要将这两个变量值交换，然后输出到控制台。
a = 10
b = 20
print("-- a, b = ", a, b)
c = a
a = b
b = c
print("-- a, b = ", a, b)

# 需求2: 现有三个变量，分别为:i=100，j=200, k=300，现需要将这三个变量值交换，将i，j，k的值分别赋值给 k，i，j，然后输出到控制台。
i = 100
j = 200 
k = 300
print("-- i, j, k = ", i, j, k)   # 100 200 300
tmp = i
i = j
j = k
k = tmp
print("-- i, j, k = ", i, j, k)   # 200 300 100